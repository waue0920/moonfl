#13~19: moon 核心代码 train_net_fedcon() ，被 local_train_net() 呼叫
def train_net_fedcon(net_id, net, global_net, 
                     previous_nets, 
                     train_dataloader, 
                     test_dataloader, epochs, lr, 
                     args_optimizer, mu, temperature, args,
                     round, device="cpu"):
    
    net = nn.DataParallel(net)
    net.cuda()
    logger.info('Training network %s' % str(net_id))
    logger.info('n_training: %d' % len(train_dataloader))
    logger.info('n_test: %d' % len(test_dataloader))

    train_acc, _ = compute_accuracy(net, train_dataloader, device=device)

    test_acc, conf_matrix, _ = compute_accuracy(net, test_dataloader, get_confusion_matrix=True, device=device)

    logger.info('>> Pre-Training Training accuracy: {}'.format(train_acc))
    logger.info('>> Pre-Training Test accuracy: {}'.format(test_acc))


    if args_optimizer == 'adam':
        optimizer = optim.Adam(filter(lambda p: p.requires_grad, net.parameters()), lr=lr, weight_decay=args.reg)
    elif args_optimizer == 'amsgrad':
        optimizer = optim.Adam(filter(lambda p: p.requires_grad, net.parameters()), lr=lr, weight_decay=args.reg,
                               amsgrad=True)
    elif args_optimizer == 'sgd':
        optimizer = optim.SGD(filter(lambda p: p.requires_grad, net.parameters()), lr=lr, momentum=0.9,
                              weight_decay=args.reg)

    #13.  Lsup = CrossEntropyLoss(Fw(x), y)
    criterion = nn.CrossEntropyLoss().cuda()
    # global_net.to(device)

    for previous_net in previous_nets:
        previous_net.cuda()
    global_w = global_net.state_dict()

    cnt = 0
    #17. sim()
    cos=torch.nn.CosineSimilarity(dim=-1)
    # mu = 0.001

    for epoch in range(epochs):
        epoch_loss_collector = []
        epoch_loss1_collector = []
        epoch_loss2_collector = []
        for batch_idx, (x, target) in enumerate(train_dataloader):
            x, target = x.cuda(), target.cuda()

            optimizer.zero_grad()
            x.requires_grad = False
            target.requires_grad = False
            target = target.long()
            # 在這邊的net 可以創建 feature map ，且可以學習。
            # 論文中可視為 Rw(x)，創建出來的feature map則是 Z，程式碼中的 pro1, pro2, pro3
            _, pro1, out = net(x)  #14 z=Rwt(x)
            _, pro2, _ = global_net(x) #15 z_glob=Rwt(x)
            posi = cos(pro1, pro2)  #17 sim(Z, Zglob) = posi
            logits = posi.reshape(-1,1)

            for previous_net in previous_nets:
                previous_net.cuda()
                _, pro3, _ = previous_net(x)  # 16 zprev = Rwt-1(x) 
                nega = cos(pro1, pro3)   #17 sim(Z, Zprev) = nega
                logits = torch.cat((logits, nega.reshape(-1,1)), dim=1)

                previous_net.to('cpu')
            #17 sim(z,zprev)/T 
            logits /= temperature
            labels = torch.zeros(x.size(0)).cuda().long()
            #18 mu * Lcon
            loss2 = mu * criterion(logits, labels)
            loss1 = criterion(out, target)
            loss = loss1 + loss2

            #19 w = w - l*grad
            loss.backward()
            optimizer.step()

            cnt += 1
            epoch_loss_collector.append(loss.item())
            epoch_loss1_collector.append(loss1.item())
            epoch_loss2_collector.append(loss2.item())

        epoch_loss = sum(epoch_loss_collector) / len(epoch_loss_collector)
        epoch_loss1 = sum(epoch_loss1_collector) / len(epoch_loss1_collector)
        epoch_loss2 = sum(epoch_loss2_collector) / len(epoch_loss2_collector)
        logger.info('Epoch: %d Loss: %f Loss1: %f Loss2: %f' % (epoch, epoch_loss, epoch_loss1, epoch_loss2))


    for previous_net in previous_nets:
        previous_net.to('cpu')
    train_acc, _ = compute_accuracy(net, train_dataloader, device=device)
    test_acc, conf_matrix, _ = compute_accuracy(net, test_dataloader, get_confusion_matrix=True, device=device)

    logger.info('>> Training accuracy: %f' % train_acc)
    logger.info('>> Test accuracy: %f' % test_acc)
    net.to('cpu') # 感覺無用
    logger.info(' ** Training complete **')
    return train_acc, test_acc

#9~19: PartyLocalTraining(i, w) 的主架構，所以包含所有演算法的local training，注意這邊的net
def local_train_net(nets, args, net_dataidx_map, 
                    train_dl=None, test_dl=None, 
                    global_model = None, prev_model_pool = None, 
                    server_c = None, clients_c = None, 
                    round=None, device="cpu"):
    avg_acc = 0.0
    acc_list = []
    if global_model:
        global_model.cuda()
    if server_c:
        server_c.cuda()
        server_c_collector = list(server_c.cuda().parameters())
        new_server_c_collector = copy.deepcopy(server_c_collector)
        
    #11: for 回圈 i in range(1 ... E):
    for net_id, net in nets.items():
        dataidxs = net_dataidx_map[net_id]

        logger.info("Training network %s. n_training: %d" % (str(net_id), len(dataidxs)))
        #10: init w
        train_dl_local, test_dl_local, _, _ = get_dataloader(args.dataset, args.datadir, args.batch_size, 32, dataidxs)
        train_dl_global, test_dl_global, _, _ = get_dataloader(args.dataset, args.datadir, args.batch_size, 32)
        n_epoch = args.epochs

        if args.alg == 'fedavg':
            trainacc, testacc = train_net(net_id, net, train_dl_local, test_dl, n_epoch, args.lr, args.optimizer, args,
                                        device=device)
        elif args.alg == 'fedprox':
            trainacc, testacc = train_net_fedprox(net_id, net, global_model, train_dl_local, test_dl, n_epoch, args.lr,
                                                  args.optimizer, args.mu, args, device=device)
        elif args.alg == 'moon':
            prev_models=[]
            #12: for b 回圈
            for i in range(len(prev_model_pool)):
                prev_models.append(prev_model_pool[i][net_id])
            #13~19:  moon 核心開始 傳輸進去的net, global_model, previous_model, 很重要，因為這邊會決定要contrastive learning的net
            trainacc, testacc = train_net_fedcon(net_id, net, 
                                                 global_model, 
                                                 prev_models, 
                                                 train_dl_local, 
                                                 test_dl, n_epoch, args.lr,
                                                 args.optimizer, args.mu, args.temperature, 
                                                 args, round, device=device)

        elif args.alg == 'local_training':
            trainacc, testacc = train_net(net_id, net, train_dl_local, test_dl, n_epoch, args.lr, args.optimizer, args,
                                          device=device)
        logger.info("net %d final test acc %f" % (net_id, testacc))
        avg_acc += testacc
        acc_list.append(testacc)
    avg_acc /= args.n_parties
    wandb.log({"avg_acc": avg_acc}) # log avg_acc
    if args.alg == 'local_training':
        logger.info("avg test acc %f" % avg_acc)
        logger.info("std acc %f" % np.std(acc_list))
    if global_model:
        global_model.to('cpu')
    if server_c:
        for param_index, param in enumerate(server_c.parameters()):
            server_c_collector[param_index] = new_server_c_collector[param_index]
        server_c.to('cpu')
    return nets

